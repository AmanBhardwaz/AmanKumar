let a = 5;

console.log(a++);  // Output: 5 (value used first, then a becomes 6)
console.log(a);    // Output: 6

console.log(++a);  // Output: 7 (a becomes 7 before use)
console.log(a);    // Output: 7

console.log(a--);  // Output: 7 (value used, then a becomes 6)
console.log(a);    // Output: 6

console.log(--a);  // Output: 5 (a becomes 5 before use)
console.log(a);    // Output: 5